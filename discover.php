<ul id="myUL" class="ul">
  <li><a href="#">Paharpur Bihar</a>
  	<p class="space"></p>
  	<p>Sompur Mahabihar or Paharpur Bihar is one of the largest well-known Buddhist Bihar in the South Asia and one of the most popular tourist destinations in the country. This very important archaeological site was designated as a UNESCO World Heritage Site in 1985. The Bihar is located at Paharpur village, Badalgachhi Upazila in Naogaon.</p>
  </li>
  <li><a href="#">Mahasthangarh</a>
  	<p class="space"></p>
  	<p>Mahasthangarh is one of the earliest urban archaeological sites so far discovered in Bangladesh. It was an important city under the Maurya Empire. The fortified area was in use until the 8th century AD.</p>
  </li>
  <li><a href="#">Shalban Vihara</a>
  	<p class="space"></p>
  	<p> Shalban Vihara, in Mainamati, Comilla, is a 7th century Paharpur-style Buddhist Vihara with 115 cells for monks. Excavations have revealed many archaeological artifacts dated to between the 7th and 12th centuries. Most of them are kept in the Mainamati Museum now.</p>
  </li>
  <li><a href="#">Sundarbans</a>
  	<p class="space"></p>
  	<p>Sunderbans, is the largest mangrove forest on earth and a UNESCO World Heritage Site. It spans from the Hooghly River in India’s state of West Bengal to the Baleswar River in Bangladesh. 60% of this mist-shrouded forest is located in Bangladesh. Intersected by a complex network of rivers and creeks, mudflats and tiny islands, this amazing forest is comprised of two elementary eco-regions: mangrove forest and freshwater swamp Forest. It has the rich biodiversity and unique eco-system which boasts of the Royal Bengal tigers, spotted deer, crocodiles, riverine dolphins, wild boars and many more.</p>
  </li>
  <li><a href="#">Sixty Dome Mosque</a>
  	<p class="space"></p>
  	<p>Sixty Dome Mosque more commonly known as Shat Gambuj Mosque is a UNESCO World Heritage Site. It is the largest mosque in Bangladesh from the Sultanate Period (1204-1576). It was built by a Saint General named Khan Jahan Ali near the coast of Bagerhat District of the Sunderbans. The construction of the mosque was started in 1442 and it was completed in 1459.</p>
  </li>
  <li><a href="#">Sreemongol</a>
  	<p class="space"></p>
  	<p>Sreemongol in Sylhet has long been credited as a top tourist attraction having earned its recognition as a town as early as the beginning of the 19th century. Even though the first ever tea garden in Sylhet – Malnichara – was established near Sylhet city in 1854. Anyone travelling in and around Sreemongol is sure to be greeted by lush green tea gardens that stretch over miles and miles of hilly areas, not to speak of the chirping birds and shading trees besides the pineapple and lemon gardens.</p>
  </li>
  <li><a href="#">Tangoar Haor</a>
  	<p class="space"></p>
  	<p>At the foot of the Meghalaya ranges in the middle of Tahirpur and Dharmapasha Upazilla under Sunamgonj district in north-east Sylhet is a vast swampland popularly known as Tangoar Haor that spans over an area of nearly 100 square kilometres. Cascading from the Meghalaya about 30 waterfalls flow into this large water body engendering a charm that is truly rare. Tangoar Haor, which is a crucial hub for biodiversity, is home to a diverse range of birds. In winter, numerous migratory birds join the local birds giving rise to an eye-soothing abundance of fauna.</p>
  </li>
  <li><a href="#">Lawachora</a>
  	<p class="space"></p>
  	<p>Lawachora is one of the seven safari parks and 10 national gardens in Bangladesh. This forest is a sanctuary for rare species of plants and animals that include endangered species of hoolok gibbon, phayre’s langur, as well as a wide variety of apes, cats and snakes including pythons, in addition to parrots, magpies and many other birds.</p>
  </li>

<li><a href="#">Jaflong</a>
  	<p class="space"></p>
  	<p>Jaflong is a ‘must-see’ destination for tourists visiting Sylhet. Flowing from the north Khasi mountains, the river Dauki enters Bangladesh under the name ‘Piyain’, along the bank of which lies the spectacular Jaflong. Much like Jaflong, Bisnakandi is mostly a quarry. This is where many layers of the Khasi mountain meet at a single point from both sides. Flowing from above is a high fall. Adding to its charm are dark clouds hugging the mountain in the rainy season.</p>
  </li>

  <li><a href="#">Cox’s Bazaar</a>
  	<p class="space"></p>
  	<p>Cox’s Bazaar is a major tourist attraction in Bangladesh. Sloping down to the blue water of the Bay of Bengal against the picturesque backdrop of a series of hills covered with dense forest, Cox’s Bazaar sea beach is the longest sea beach in the world. Its total length is 120 km. It is the place of miles of golden sands, lofty cliffs, surfing waves, amazing conch shells, beautiful pagodas, Buddhist temples and tribes, and mouthwatering seafood. The shark-free beach has its own reputation for bathing, sunbathing, swimming, surfing and many beach rides. The breathtaking beauty of the setting sun behind the vast sea is fascinating.</p>
  </li>

  <li><a href="#">Saint Martin Island</a>
  	<p class="space"></p>
  	<p> The Saint Martin’s Island is one of the most visited tourist’s spots in Bangladesh. The only coral island in Bangladesh is about 8 km in length and rarely more than 1 km wide. It is about 10 km (6 mi) south-west of the southern tip of the Cox’s Bazaar-Teknaf peninsula. Beaches fringed with coconut palms, panoramic beauty of the island and pristine marine life attract the tourists. Magnificent landscapes, crystal clear sea water, coral colony, and the roar of the Bay of Bengal are the main attraction of the visitors.</p>
  </li>

  <li><a href="#">Bandarban</a>
  	<p class="space"></p>
  	<p>Bandarban, literally meaning ‘dam of monkeys’, is a district of Bangladesh in Chittagong Division in Chittagong Hill Tracts area. It has some of the most fascinating tourist attractions of Bangladesh. The beautiful mountain ranges escalate the natural beauty of the surroundings. The largest Buddhist temple of Bangladesh known as Buddha Dhatu Jadi is situated in Bandarban. Also, the peaks like Nilgiri and Thanchi captivate the eyes of the tourists immensely.</p>
  </li>

  <li><a href="#">Rangamati</a>
  	<p class="space"></p>
  	<p> Known as the ‘Lake City’ of Bangladesh, Rangamati is full of matchless beauty and is one of the most visited tourist destinations of Bangladesh. It is located about 77 kilometers away from Chittagong. Rangamati Town, Sajek valley, Hanging Bridge, Kaptai Lake, and Indigenous Museum are among the notable landmarks and popular tourist destinations of Rangamati.</p>
  </li>

  <li><a href="#">Khagrachari</a>
  	<p class="space"></p>
  	<p>Khagrachari, locally known as Chengmi is one of the most beautiful districts in Bangladesh. This beautiful district competes shoulder to shoulder with Bandarban and Rangamati in terms of mesmerizing landscapes and vibrant culture and lifestyles. Richhang Waterfall, Alutilla cave, Dighinala are some of the attractions located in Khagrachari.</p>
  </li>

  <li><a href="#">Nijhum Dwip</a>
  	<p class="space"></p>
  	<p>In the south of Bangladesh, a smattering of islands sits at the intersection of the Meghna river and the Bay of Bengal. Nijhum Dwip, which means “Silent Island”, is a small island at the southern tip of Hatiya sub-district. Though a decent number of domestic tourists make it down to the island, it’s not well known to foreigners. The Silent Island is best known for its national park with a significant spotted deer population. Pleasant as the park was, I found the island more notable for its remote location and the friendliness of its people. I was on a mission to find a green place where I could wander around and enjoy Bangladeshi village life, and on Nijhum Dwip, it was all too easy.</p>
  </li>

  <li><a href="#">Kuakata</a>
  	<p class="space"></p>
  	<p>Kuakata is a town in southern Bangladesh known for its panoramic sea beach. Kuakata beach is a sandy expanse 18 kilometres long and 3 kilometres wide. From the beach one can have an unobstructed view of both sunrise and sunset over the Bay of Bengal.</p>
  </li>

